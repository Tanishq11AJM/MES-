MOV 50H, #03H
MOV 51H, #03H
MOV A, 50H
MOV R0, A 
MOV A, 51H
MOV R1, A 
COMPARE_LOOP:
	MOV A, R0 
	JZ CHECK_B_ZERO
	MOV A, R1 
	JZ A_GREATER
	DEC R0
	DEC R1
	MOV A, #01H 
	JNZ COMPARE_LOOP
CHECK_B_ZERO:
	MOV A, R1
	JZ BOTH_EQUAL
	MOV R2, #0FFH
	MOV A, #01H
	JNZ STOP_PROGRAM
A_GREATER:
	MOV R2, 01H
	MOV A, #01H
	JNZ STOP_PROGRAM
BOTH_EQUAL:
	MOV R2, #00H
STOP_PROGRAM:
	MOV A, #01H
	JNZ STOP_PROGRAM
END
